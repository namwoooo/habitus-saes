def _normalize(series: pd.Series) -> pd.Series:
    mn, mx = series.min(), series.max()
    if mx - mn < 1e-9:
        return pd.Series(np.zeros(len(series)), index=series.index)
    return (series - mn) / (mx - mn)


def _sigmoid_normalize(arr: np.ndarray, scale: float = 6.0) -> np.ndarray:
    """
    Sigmoid 정규화.
    극단값 압축 + 중간 구간 확보 → 단봉 분포 유도.
    engagement와 GT가 같은 feat_를 쓰더라도
    비선형 변환으로 분포 형태가 달라져 독립성 부분 확보.
    """
    center      = np.median(arr)
    transformed = 1.0 / (1.0 + np.exp(-scale * (arr - center)))
    mn, mx      = transformed.min(), transformed.max()
    if mx - mn < 1e-6:
        return arr.copy()
    return (transformed - mn) / (mx - mn)


def smooth_and_normalize(df, cols, smooth_sec=2.0):
    dt = df['timestamp_sec'].diff().median()
    if pd.isna(dt) or dt <= 0:
        dt = 0.033
    win = max(1, int(smooth_sec / dt))
    for col in cols:
        df[col] = uniform_filter1d(df[col].values.astype(float), size=win)
        df[col] = _normalize(df[col])
    return df


def discretize(df, cols, thresholds=None):
    default_thr = {
        'engagement': (0.35, 0.65),
        'confusion':  (0.30, 0.60),
        'fatigue':    (0.30, 0.60),
    }
    thr = {**default_thr, **(thresholds or {})}
    for col in cols:
        lo, hi = thr.get(col, (0.33, 0.67))
        df[f'{col}_label'] = np.where(
            df[col] < lo, 0, np.where(df[col] < hi, 1, 2)
        )
    return df


