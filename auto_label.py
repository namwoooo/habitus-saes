def extract_gaze_features(df: pd.DataFrame,
                           scenario: str,
                           window_sec: float = 3.0) -> pd.DataFrame:
    """
    세 시나리오 공통 feature 추출.
    시나리오별 추가 feature는 별도 섹션에서 계산.
    """
    df  = df.copy().sort_values('timestamp_sec').reset_index(drop=True)
    dt  = df['timestamp_sec'].diff().median()
    win = max(1, int(window_sec / dt)) if dt and dt > 0 else 30

    dx = df['gaze_x'].diff()
    dy = df['gaze_y'].diff()

    # ── 공통: 수평 이동 ───────────────────────────────────────────────────
    move_right = (dx > 0).astype(float)
    move_left  = (dx < 0).astype(float)
    df['feat_horizontal_consistency'] = (
        move_right.rolling(win, min_periods=1).mean()
        - move_left.rolling(win, min_periods=1).mean()
    )
    df['feat_backtrack_ratio'] = move_left.rolling(win, min_periods=1).mean()

    # ── 공통: 시선 분산 ───────────────────────────────────────────────────
    sx = df['gaze_x'].rolling(win, min_periods=1).std().fillna(0)
    sy = df['gaze_y'].rolling(win, min_periods=1).std().fillna(0)
    df['feat_gaze_dispersion'] = np.sqrt(sx**2 + sy**2)

    # 강의: 분산 역수 = 안정성
    df['feat_gaze_stability'] = 1.0 / (df['feat_gaze_dispersion'] + 1.0)

    # ── 공통: 안구 도약 속도 ──────────────────────────────────────────────
    speed = np.sqrt(dx**2 + dy**2) / df['timestamp_sec'].diff().replace(0, np.nan)
    df['feat_saccade_velocity_mean'] = speed.rolling(win, min_periods=1).mean().fillna(0)

    # ── 공통: 고정(fixation) ──────────────────────────────────────────────
    threshold = 20
    if all(c in df.columns for c in ['screen_x_px', 'screen_y_px']):
        pixel_dist  = np.sqrt(
            df['screen_x_px'].diff()**2 + df['screen_y_px'].diff()**2
        ).fillna(0)
        is_fixation = (pixel_dist < threshold).astype(float)
        df['feat_fixation_duration_mean'] = (
            (is_fixation * dt).rolling(win, min_periods=1).sum()
            / is_fixation.rolling(win, min_periods=1).sum().replace(0, np.nan)
        ).fillna(0)
    else:
        df['feat_fixation_duration_mean'] = 0.0

    # ── 공통: Iris 움직임 ─────────────────────────────────────────────────
    if all(c in df.columns for c in ['iris_L_x_px','iris_L_y_px','iris_R_x_px','iris_R_y_px']):
        iris_dx = (df['iris_R_x_px'] - df['iris_L_x_px']).diff().abs()
        iris_dy = (df['iris_R_y_px'] - df['iris_L_y_px']).diff().abs()
        df['feat_iris_movement'] = (iris_dx + iris_dy).rolling(win, min_periods=1).mean().fillna(0)
    else:
        df['feat_iris_movement'] = 0.0

    # ── 공통: 섹션 스코어 ─────────────────────────────────────────────────
    df['feat_section_score_mean'] = (
        df.get('gaze_section_score', pd.Series(0.5, index=df.index))
        .rolling(win, min_periods=1).mean().fillna(0)
    )
    df['feat_top_seg_score_mean'] = (
        df.get('seg1_score', pd.Series(0.5, index=df.index))
        .rolling(win, min_periods=1).mean().fillna(0)
    )

    # ── 공통: 섹션 일관성 & 재방문율 ─────────────────────────────────────
    gaze_sec     = df.get('gaze_section', pd.Series('', index=df.index))
    same_section = (gaze_sec == gaze_sec.shift(1)).astype(float)
    df['feat_section_consistency'] = same_section.rolling(win, min_periods=1).mean()

    def revisit_rate(series, w):
        rates = []
        for i in range(len(series)):
            start  = max(0, i - w + 1)
            window = series.iloc[start:i+1]
            if len(window) < 2:
                rates.append(0.0); continue
            seen, revisit = set(), 0
            for v in window:
                if v in seen: revisit += 1
                seen.add(v)
            rates.append(revisit / (len(window) - 1))
        return pd.Series(rates, index=series.index)

    df['feat_revisit_rate'] = revisit_rate(gaze_sec.fillna(''), win)

    # ── 시나리오별 추가 feature ───────────────────────────────────────────

    if scenario == 'paper':
        if 'scrollY' in df.columns:
            scroll_diff       = df['scrollY'].diff().fillna(0)
            scroll_down_ratio = (scroll_diff > 0).astype(float).rolling(win, min_periods=1).mean()
            scroll_std        = scroll_diff.rolling(win, min_periods=1).std().fillna(0)
            max_std           = scroll_std.max() if scroll_std.max() > 0 else 1.0
            df['feat_scroll_regularity'] = (
                scroll_down_ratio * 0.5 + (1.0 - scroll_std / max_std) * 0.5
            )
        else:
            df['feat_scroll_regularity'] = 0.5

        # outside 비율
        if 'gaze_section' in df.columns:
            df['feat_outside_ratio'] = (
                df['gaze_section'].isin(OUTSIDE_SECTIONS).astype(float)
                .rolling(win, min_periods=1).mean()
            )
        else:
            df['feat_outside_ratio'] = 0.0

    elif scenario == 'coding':
        # 마우스 신호
        has_mouse = ('mouse_x' in df.columns) and ('mouse_y' in df.columns)
        if has_mouse:
            dt_series = df['timestamp_sec'].diff().replace(0, np.nan)
            mdx       = df['mouse_x'].diff()
            mdy       = df['mouse_y'].diff()
            dist      = np.sqrt(mdx**2 + mdy**2).fillna(0)
            df['feat_mouse_speed_mean']    = (dist / dt_series).fillna(0).rolling(win, min_periods=1).mean()
            df['feat_mouse_travel_sum']    = dist.rolling(win, min_periods=1).sum()
            if all(c in df.columns for c in ['screen_x_px', 'screen_y_px']):
                df['feat_gaze_mouse_gap_mean'] = np.sqrt(
                    (df['screen_x_px'] - df['mouse_x'])**2 +
                    (df['screen_y_px'] - df['mouse_y'])**2
                ).fillna(0).rolling(win, min_periods=1).mean()
            else:
                df['feat_gaze_mouse_gap_mean'] = 0.0
        else:
            df['feat_mouse_speed_mean']    = 0.0
            df['feat_mouse_travel_sum']    = 0.0
            df['feat_gaze_mouse_gap_mean'] = 0.0

        if 'gaze_section' in df.columns:
            df['feat_outside_ratio'] = (
                df['gaze_section'].isin(OUTSIDE_SECTIONS).astype(float)
                .rolling(win, min_periods=1).mean()
            )
        else:
            df['feat_outside_ratio'] = 0.0

    elif scenario == 'lecture':
        if 'gaze_section' in df.columns:
            is_video   = df['gaze_section'].isin(VIDEO_SECTIONS).astype(float)
            is_outside = df['gaze_section'].isin(OUTSIDE_SECTIONS).astype(float)
            df['feat_video_watch_ratio'] = is_video.rolling(win, min_periods=1).mean()
            df['feat_outside_ratio']     = is_outside.rolling(win, min_periods=1).mean()
            df['feat_video_miss_ratio']  = 1.0 - df['feat_video_watch_ratio']

            prev_sec  = gaze_sec.shift(1)
            is_switch = (
                (gaze_sec != prev_sec) &
                (gaze_sec.isin(NOTE_SECTIONS) | gaze_sec.isin(VIDEO_SECTIONS))
            ).astype(float)
            df['feat_note_switch']      = is_switch.rolling(win, min_periods=1).mean()
            section_change              = (gaze_sec != prev_sec).astype(float)
            df['feat_periodic_pattern'] = section_change.rolling(win * 2, min_periods=1).mean()
        else:
            df['feat_video_watch_ratio'] = 0.5
            df['feat_outside_ratio']     = 0.5
            df['feat_video_miss_ratio']  = 0.5
            df['feat_note_switch']       = 0.0
            df['feat_periodic_pattern']  = 0.0

    return df


