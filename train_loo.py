def run_loo_cv(dfs: list) -> pd.DataFrame:
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    print(f"\n디바이스: {DEVICE}")
    print(f"입력 크기: scale({len(SCALE_COLS)}) + scenario({len(SCENARIO_COLS)}) = {len(FEATURE_COLS)}")
    print(f"타겟: objective_gt (단일 회귀)")
    print("=" * 65)

    loo_results      = []
    all_fold_preds   = {}
    all_fold_targets = {}

    for test_idx in range(len(dfs)):
        torch.manual_seed(SEED)
        np.random.seed(SEED)

        if torch.cuda.is_available():
            torch.cuda.manual_seed_all(SEED)

        train_idxs = [i for i in range(len(dfs)) if i != test_idx]
        print(f"\n[Fold {test_idx+1}]  Test: {test_idx+1}  |  Train: {[i+1 for i in train_idxs]}")

        ram_before = get_ram_mb()

        # scaler: train 데이터의 scale_cols만 fit
        train_concat = pd.concat([dfs[i] for i in train_idxs], ignore_index=True)
        scaler = StandardScaler()
        scaler.fit(train_concat[SCALE_COLS].values)

        # 시퀀스 생성
        X_tr_list, y_tr_list = [], []
        for i in train_idxs:
            Xs, ys = make_sequences(dfs[i], scaler, fit=False)
            X_tr_list.append(Xs); y_tr_list.append(ys)
        X_train = np.concatenate(X_tr_list)
        y_train = np.concatenate(y_tr_list)
        X_test, y_test = make_sequences(dfs[test_idx], scaler, fit=False)

        print(f"  Train: {X_train.shape}  |  Test: {X_test.shape}")

        # Validation split
        X_tr, X_val, y_tr, y_val = train_test_split(
          X_train,
          y_train,
          test_size=0.15,
          random_state=SEED,
          shuffle=True
      )

        generator = torch.Generator()
        generator.manual_seed(SEED)

        train_loader = DataLoader(
            GazeDataset(X_tr, y_tr),
            batch_size=BATCH_SIZE,
            shuffle=True,
            generator=generator
        )
        val_loader   = DataLoader(GazeDataset(X_val, y_val), batch_size=BATCH_SIZE)
        test_loader  = DataLoader(GazeDataset(X_test, y_test), batch_size=BATCH_SIZE)

        # 모델 초기화
        model     = EarlyFusionBiLSTM().to(DEVICE)
        optimizer = torch.optim.AdamW(model.parameters(), lr=LR, weight_decay=1e-4)
        scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(optimizer, patience=5, factor=0.5)
        criterion = nn.MSELoss()

        # 학습
        best_val, best_state, no_improve = float('inf'), None, 0
        train_losses, val_losses = [], []
        train_start = time.perf_counter()

        for epoch in range(1, EPOCHS + 1):
            tr_loss = train_epoch(model, train_loader, optimizer, criterion)
            vl_loss, _, _ = eval_epoch(model, val_loader, criterion)
            scheduler.step(vl_loss)
            train_losses.append(tr_loss); val_losses.append(vl_loss)

            MIN_DELTA = 1e-4

            if best_val - vl_loss > MIN_DELTA:
                best_val   = vl_loss
                best_state = {k: v.clone() for k, v in model.state_dict().items()}
                no_improve = 0
            else:
                no_improve += 1

            if epoch % 10 == 0:
                print(f"  Epoch {epoch:3d} | train={tr_loss:.4f} | val={vl_loss:.4f} | lr={optimizer.param_groups[0]['lr']:.2e}")
            if no_improve >= PATIENCE:
                print(f"  Early stopping @ epoch {epoch}"); break

        train_time = time.perf_counter() - train_start

        # 테스트 평가
        model.load_state_dict(best_state)
        _, preds, targets = eval_epoch(model, test_loader, criterion)
        infer_ms  = measure_infer_per_seq_ms(model, X_test)
        ram_delta = get_ram_mb() - ram_before

        all_fold_preds[test_idx]   = preds
        all_fold_targets[test_idx] = targets

        # 메트릭
        mae  = mean_absolute_error(targets, preds)
        rmse = np.sqrt(mean_squared_error(targets, preds))
        r2   = r2_score(targets, preds)

        fold_metrics = {
            'fold':             test_idx + 1,
            'objective_gt_MAE': round(mae,  4),
            'objective_gt_RMSE':round(rmse, 4),
            'objective_gt_R2':  round(r2,   4),
            'train_time_sec':   round(train_time, 2),
            'infer_per_seq_ms': round(infer_ms, 5),
            'ram_delta_mb':     round(ram_delta, 1),
        }
        print(f"\n  ── 테스트 결과 (Fold {test_idx+1}) ──")
        print(f"  [objective_gt]  MAE={mae:.4f}  RMSE={rmse:.4f}  R²={r2:.4f}")
        print(f"  학습={train_time:.1f}s  |  추론/seq={infer_ms:.5f}ms  |  RAM Δ={ram_delta:+.1f}MB")
        loo_results.append(fold_metrics)

        # Loss 곡선
        fig, ax = plt.subplots(figsize=(7, 3))
        ax.plot(train_losses, color='#378ADD', lw=1.8, label='Train')
        ax.plot(val_losses,   color='#D85A30', lw=1.8, label='Val')
        ax.set_title(f'Fold {test_idx+1} — Loss Curve (objective_gt)')
        ax.set_xlabel('Epoch'); ax.set_ylabel('MSE Loss')
        ax.legend(); ax.grid(alpha=0.3)
        ax.spines[['top', 'right']].set_visible(False)
        plt.tight_layout()
        plt.savefig(os.path.join(OUTPUT_DIR, f'loss_fold{test_idx+1}.png'), dpi=120)
        plt.show()

    plot_all_folds(all_fold_preds, all_fold_targets)

    results_df = pd.DataFrame(loo_results)
    print(f"\n{'='*65}")
    print("LOO-CV 최종 결과 (objective_gt 단일 회귀)")
    print('='*65)
    print(results_df.to_string(index=False))
    print("\n[전체 평균]")
    print(results_df.drop(columns='fold').mean().to_string())

    results_df.to_csv(os.path.join(OUTPUT_DIR, 'loo_results_objective_gt.csv'), index=False)
    print(f"\n결과 저장: {OUTPUT_DIR}/loo_results_objective_gt.csv")
    return results_df


