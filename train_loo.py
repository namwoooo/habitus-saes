def make_sequences(df: pd.DataFrame, scaler: StandardScaler, fit: bool = False):
    """
    시나리오 원핫은 정규화 제외.
    X : (N, SEQ_LEN, len(SCALE_COLS) + len(SCENARIO_COLS))
    y : (N, 1)  objective_gt
    """
    X_scale = df[SCALE_COLS].values.astype(np.float32)
    X_scene = df[SCENARIO_COLS].values.astype(np.float32)   # 정규화 제외
    y_raw   = df[TARGET_COLS].values.astype(np.float32)

    if fit:
        X_scale = scaler.fit_transform(X_scale)
    else:
        X_scale = scaler.transform(X_scale)

    # 정규화된 feature + 시나리오 원핫 합치기
    X_raw = np.concatenate([X_scale, X_scene], axis=1)

    X_seqs, y_seqs = [], []
    for start in range(0, len(X_raw) - SEQ_LEN + 1, STRIDE):
        end = start + SEQ_LEN
        X_seqs.append(X_raw[start:end])
        y_seqs.append(y_raw[end - 1])

    return np.array(X_seqs), np.array(y_seqs)


