def plot_all_folds(all_preds, all_targets):
    n_folds = len(all_preds)
    n_show  = 150
    COLOR   = '#378ADD'

    fig = plt.figure(figsize=(5 * n_folds, 4))
    fig.suptitle('Early Fusion BiLSTM — objective_gt 예측 vs 실제', fontsize=13)
    gs = gridspec.GridSpec(1, n_folds, hspace=0.4, wspace=0.3)

    for fold_i in range(n_folds):
        ax  = fig.add_subplot(gs[0, fold_i])
        tgt = all_targets[fold_i][:n_show, 0]
        pred= all_preds[fold_i][:n_show, 0]

        ax.plot(tgt,  color=COLOR, lw=1.5, alpha=0.9, label='실제')
        ax.plot(pred, color=COLOR, lw=1.2, alpha=0.6, linestyle='--', label='예측')

        mae = mean_absolute_error(all_targets[fold_i], all_preds[fold_i])
        r2  = r2_score(all_targets[fold_i], all_preds[fold_i])
        ax.set_title(f'Fold {fold_i+1}\nMAE={mae:.3f}  R²={r2:.3f}', fontsize=9)
        ax.set_ylim(-0.05, 1.05)
        ax.grid(alpha=0.3); ax.spines[['top', 'right']].set_visible(False)
        if fold_i == 0:
            ax.legend(fontsize=7, loc='upper right')

    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, 'predictions_objective_gt.png'), dpi=120, bbox_inches='tight')
