def normalize_gt_within_scenario(dfs: list, file_paths: list) -> list:
    """
    시나리오별 피험자 간 objective_gt minmax 정규화.

    [왜 필요한가]
      coding_namwoo와 coding_yoonjo의 GT 절대 스케일이 달라서
      yoonjo로 학습한 모델이 namwoo를 예측할 때 범위를 벗어남.
      → 시나리오 내 전체 GT를 0~1로 맞춰 스케일 통일.

    [시나리오 경계를 넘지 않는 이유]
      coding GT와 paper GT를 같이 정규화하면 시나리오 특성이 섞임.
      → coding끼리, paper끼리, lecture끼리 독립 정규화.

    [FILE_PATHS 에서 시나리오 자동 추출]
      파일명에 'coding' / 'paper' / 'lecture' 가 포함되어 있다고 가정.
      예: autogaze_labeled_coding_v4_namwoo.csv → 'coding'
    """
    import re

    # 파일명에서 시나리오 추출
    def extract_scenario(path: str) -> str:
        fname = os.path.basename(path).lower()
        if 'coding'  in fname: return 'coding'
        if 'lecture' in fname: return 'lecture'
        if 'paper'   in fname: return 'paper'
        return 'unknown'

    scenarios = [extract_scenario(p) for p in file_paths]

    # 시나리오별 인덱스 묶기
    scenario_groups = {}
    for idx, scen in enumerate(scenarios):
        scenario_groups.setdefault(scen, []).append(idx)

    print("\n" + "="*60)
    print("⚙️  시나리오별 GT 정규화")
    print("="*60)

    dfs_out = [df.copy() for df in dfs]

    for scen, indices in scenario_groups.items():
        # 시나리오 내 전체 GT 수집
        all_gt = np.concatenate([dfs[i]['objective_gt'].values for i in indices])
        gt_min, gt_max = all_gt.min(), all_gt.max()

        print(f"\n  [{scen}] 피험자 {len(indices)}명")
        print(f"    정규화 전: mean={all_gt.mean():.3f}  std={all_gt.std():.3f}  "
              f"range=[{gt_min:.3f}, {gt_max:.3f}]")

        if gt_max - gt_min < 1e-6:
            print(f"    ⚠️  GT 범위가 너무 좁음 → 정규화 스킵")
            continue

        # 각 파일의 GT를 시나리오 내 전체 범위로 정규화
        for i in indices:
            fname = os.path.basename(file_paths[i])
            gt_before = dfs_out[i]['objective_gt'].values.copy()
            gt_normed = np.clip((gt_before - gt_min) / (gt_max - gt_min), 0, 1)
            dfs_out[i]['objective_gt'] = gt_normed

            print(f"    {fname}")
            print(f"      before: mean={gt_before.mean():.3f}  std={gt_before.std():.3f}")
            print(f"      after:  mean={gt_normed.mean():.3f}  std={gt_normed.std():.3f}")

        # 정규화 후 시나리오 전체 통계
        all_gt_after = np.concatenate([dfs_out[i]['objective_gt'].values for i in indices])
        print(f"    정규화 후: mean={all_gt_after.mean():.3f}  std={all_gt_after.std():.3f}  "
              f"range=[{all_gt_after.min():.3f}, {all_gt_after.max():.3f}]")

    print("\n✅ GT 정규화 완료\n")
    return dfs_out


