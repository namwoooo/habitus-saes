def add_scenario_onehot(df: pd.DataFrame) -> pd.DataFrame:
    """
    scenario 컬럼(0=paper, 1=lecture, 2=coding)을 원핫으로 변환.
    scenario 컬럼이 없으면 모두 0으로 채움.
    """
    mapping = {0: 'paper', 1: 'lecture', 2: 'coding'}
    for col in SCENARIO_COLS:
        df[col] = 0.0

    if 'scenario' in df.columns:
        for code, name in mapping.items():
            mask = df['scenario'] == code
            df.loc[mask, f'scenario_{name}'] = 1.0
    else:
        # scenario 컬럼 없으면 파일명으로 추정
        print("  ⚠️  scenario 컬럼 없음 → 원핫 0으로 설정 (파일별 수동 확인 필요)")

    return df


def load_data(file_paths: list) -> list:
    dfs = []
    for path in file_paths:
        df = pd.read_csv(path).sort_values('timestamp_sec').reset_index(drop=True)

        # 시나리오 원핫 추가
        df = add_scenario_onehot(df)

